	echo `tput bold``tput setaf 7`"Contraseña: `tput setaf 2`$pass"`tput sgr 0`
	sleep 1
	echo `tput bold``tput setaf 7`"¿Son los datos correctos? `tput setaf 2`s/n"`tput sgr 0`
	read sn
	if [ "$sn" = "s" ] || [ "$sn" = "S" ]
		then
		clear
		echo "$name:$pass:100:normal">> usuarios.txt
		sleep 1
		echo `tput bold`"Guardando..."`tput sgr 0`
		sleep 1
		clear
		echo `tput bold`"Listo, ya puedes entrar con este usuario."`tput sgr 0`
		sleep 1
		menu
	elif [ "$sn" = "n" ] || [ "$sn" = "N" ]
		then
		clear
		echo `tput bold``tput setaf 1`"Vuelve a intentarlo."`tput sgr 0`
		sleep 1
		registro
	else
		echo `tput bold``tput setaf 7`"Ingrese `tput setaf 2`S o N, `tput setaf 1`$sn no es una opción correcta."`tput sgr 0`
	fi
}

